public class Contacto {

		public Contacto(){

		}

		private String nombre;
		private String apellido;
		
		public String getNombre() {

			return nombre;
		}

		public void setNombre (String idnombre) {

			nombre=idnombre;
		}

		public String getApellido () {

			return apellido;
		}

		public void setApellido (String idapellido) {

			apellido=idapellido;
		}

		
}


